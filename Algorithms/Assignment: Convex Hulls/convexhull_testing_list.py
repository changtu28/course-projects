def theta(pointA, pointB):
    """Computs an approximation of the angle between
       the line AB and a horizontal line through A
    """
    dx = pointB[0] - pointA[0]
    dy = pointB[1] - pointA[1]
    if abs(dx) < 1.e-6 and abs(dy) < 1.e-6:
        t = 0
    else:
        t = dy / (abs(dx) + abs(dy))
        
    if dx < 0:
        t = 2 - t
    elif dy < 0:
        t = 4 + t
    elif t == 0:
        return 360
    return t * 90

